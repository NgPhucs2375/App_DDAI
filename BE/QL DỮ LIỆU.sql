CREATE TABLE NguoiDung (
    MaNguoiDung CHAR(5) PRIMARY KEY,
    HoTen NVARCHAR(100) NOT NULL,
    Email NVARCHAR(100) UNIQUE NOT NULL,
    MatKhau NVARCHAR(255) NOT NULL,
    Tuoi INT CHECK (Tuoi >= 0),
    GioiTinh NVARCHAR(10) CHECK (GioiTinh IN (N'Nam', N'Nữ', N'Khác')),
    ChieuCao FLOAT,
    CanNang FLOAT,
    MucDoVanDong NVARCHAR(20),
    NgayTao DATE DEFAULT GETDATE()
);

CREATE TABLE LoaiThucPham (
    MaLoai CHAR(5) PRIMARY KEY,
    TenLoai NVARCHAR(100) NOT NULL
);

CREATE TABLE ThucPham (
    MaThucPham CHAR(5) PRIMARY KEY,
    TenThucPham NVARCHAR(100) NOT NULL,
    DonVi NVARCHAR(50) DEFAULT N'100g',
    Calories FLOAT,
    Protein FLOAT,
    Carbs FLOAT,
    ChatBeo FLOAT,
    ChatXo FLOAT,
    Vitamin NVARCHAR(200),
    MaLoai CHAR(5) FOREIGN KEY REFERENCES LoaiThucPham(MaLoai)
);

CREATE TABLE BuaAn (
    MaBuaAn CHAR(5) PRIMARY KEY,
    MaNguoiDung CHAR(5) FOREIGN KEY REFERENCES NguoiDung(MaNguoiDung),
    NgayAn DATE NOT NULL,
    LoaiBua NVARCHAR(20) CHECK (LoaiBua IN (N'Sáng', N'Trưa', N'Tối', N'Snack'))
);

CREATE TABLE ChiTietBuaAn (
    MaChiTiet CHAR(5) PRIMARY KEY,
    MaBuaAn CHAR(5) FOREIGN KEY REFERENCES BuaAn(MaBuaAn),
    MaThucPham CHAR(5) FOREIGN KEY REFERENCES ThucPham(MaThucPham),
    SoLuong FLOAT NOT NULL
);

CREATE TABLE CongThuc (
    MaCongThuc CHAR(5) PRIMARY KEY,
    TenMon NVARCHAR(100) NOT NULL,
    HuongDan NVARCHAR(MAX),
    GhiChu NVARCHAR(200)
);

CREATE TABLE ChiTietCongThuc (
    MaChiTiet CHAR(5) PRIMARY KEY,
    MaCongThuc CHAR(5) FOREIGN KEY REFERENCES CongThuc(MaCongThuc),
    MaThucPham CHAR(5) FOREIGN KEY REFERENCES ThucPham(MaThucPham),
    SoLuong FLOAT NOT NULL
);

CREATE TABLE MucTieu (
    MaMucTieu CHAR(5) PRIMARY KEY,
    MaNguoiDung CHAR(5) FOREIGN KEY REFERENCES NguoiDung(MaNguoiDung),
    LoaiMucTieu NVARCHAR(50),
    CaloriesMucTieu FLOAT,
    ProteinMucTieu FLOAT,
    CarbsMucTieu FLOAT,
    ChatBeoMucTieu FLOAT,
    NgayBatDau DATE,
    NgayKetThuc DATE
);

